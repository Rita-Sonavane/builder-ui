export class IconPropertyDescriptor{


    private iconClass:any;
    private id:any;
    private outerId: any;
    private iconColor:any;
    private iconSize:any;
    
    public getIconClass(): any {
        return this.iconClass;
    }

    public setIconClass(iconClass: any): void {
        this.iconClass = iconClass;
    }

    public getId(): any {
        return this.id;
    }

    public setId(id: any): void {
        this.id = id;
    }
    public getOuterId(): any {
        return this.outerId;
    }

    public setOuterId(outerId: any): void {
        this.outerId = outerId;
    }

    public getIconColor(): any {
        return this.iconColor;
    }

    public setIconColor(iconColor: any): void {
        this.iconColor = iconColor;
    }

    public getIconSize(): any {
        return this.iconSize;
    }

    public setIconSize(iconSize: any): void {
        this.iconSize = iconSize;
    }



}